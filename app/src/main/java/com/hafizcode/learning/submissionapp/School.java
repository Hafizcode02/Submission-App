package com.hafizcode.learning.submissionapp;

public class School {
    private String namaSekolah;
    private String alamatSekolah;
    private String visiSekolah;
    private String telpSekolah;
    private String emailSekolah;
    private String webSekolah;
    private int photoSekolah;

    public String getNamaSekolah() {
        return namaSekolah;
    }

    public void setNamaSekolah(String namaSekolah) {
        this.namaSekolah = namaSekolah;
    }

    public String getAlamatSekolah() {
        return alamatSekolah;
    }

    public void setAlamatSekolah(String alamatSekolah) {
        this.alamatSekolah = alamatSekolah;
    }

    public String getVisiSekolah() {
        return visiSekolah;
    }

    public void setVisiSekolah(String visiSekolah) {
        this.visiSekolah = visiSekolah;
    }

    public String getTelpSekolah() {
        return telpSekolah;
    }

    public void setTelpSekolah(String telpSekolah) {
        this.telpSekolah = telpSekolah;
    }

    public String getEmailSekolah() {
        return emailSekolah;
    }

    public void setEmailSekolah(String emailSekolah) {
        this.emailSekolah = emailSekolah;
    }

    public String getWebSekolah() {
        return webSekolah;
    }

    public void setWebSekolah(String webSekolah) {
        this.webSekolah = webSekolah;
    }

    public int getPhotoSekolah() {
        return photoSekolah;
    }

    public void setPhotoSekolah(int photoSekolah) {
        this.photoSekolah = photoSekolah;
    }

}
